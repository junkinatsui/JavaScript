

let weather = "晴れ";

switch (weather) {
    case "晴れ":
        console.log("今日の天気は晴れです。");
        break;
    case "曇り":
        console.log("今日は曇りです。");
        break;
    case "雨":
        console.log("今日は雨です。");
        break;
    default:
        console.log("天気が不明です。");
}

